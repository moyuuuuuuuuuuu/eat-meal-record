    /**
     * 根据标签名列表生成一条提示语
     *
     * @param  string[] $tagNames  匹配到的标签 name 字段列表
     * @return string
     */
    public function generate(array $tagNames): string
    {
        if (empty($tagNames)) {
            return '保持均衡饮食是健康的基础，继续加油！';
        }

        $candidates = [];

        foreach ($tagNames as $tagName) {
            foreach (self::TIPS_MAP as $keyword => $tip) {
                // 标签名包含关键词即命中，同一 keyword 只收录一次
                if (!isset($candidates[$keyword]) && mb_strpos($tagName, $keyword) !== false) {
                    $candidates[$keyword] = $tip;
                }
            }
        }

        if (empty($candidates)) {
            return '基于您的饮食习惯，建议保持食物多样性。';
        }

        return $candidates[array_rand($candidates)];
    }
}
